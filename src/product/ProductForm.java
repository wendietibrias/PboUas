    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        // TODO add your handling code here:
        String nameProduct = productName.getText();
        String sku = skuNumber.getText();
        String price = productPrice.getText();
        
        int convertedPrice = Integer.parseInt(price);
        String categoryName = categoryId.getSelectedItem().toString().split(" ")[0];
        int categoryId = Integer.parseInt(categoryName);
        
        ProductModel productModel = new ProductModel();
        productModel.createProduct(nameProduct, sku, convertedPrice, categoryId);
        
        boolean isCreated = productController.handleSubmitProduct(productModel,this.selectedId);
        
        if(isCreated){
            if(this.selectedId.length() > 0){
                this.selectedId =  "";
                JOptionPane.showMessageDialog(null,"Berhasil Mengedit Produk");

            } else {
               JOptionPane.showMessageDialog(null,"Berhasil Menambahkan Produk");
            }
            
            new ProductForm("").setVisible(false);
            new ProductIndex().setVisible(true);
        } else {
           JOptionPane.showMessageDialog(null,"Gagal Menambahkan Produk");
        }
    }//GEN-LAST:event_jButton2MouseClicked

