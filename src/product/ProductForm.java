    public ProductForm(String id) {
        initComponents();
        
        if(id.length() > 0){
            this.selectedId = id;
        }
        
        try {
          productController = new ProductController();
          
          
          ArrayList<String> kategoriLists = productController.getCategories();
          
          for(int i = 0; i < kategoriLists.size(); i++) {
              categoryId.addItem(kategoriLists.get(i));
          }
          
          
          if(id.length() > 0){
              ProductModel productModel = productController.getOne(this.selectedId);
              productName.setText(productModel.name);
              skuNumber.setText(productModel.sku);
              productPrice.setText(String.valueOf(productModel.price));
              categoryId.setSelectedIndex(productModel.categoryId);
          }
          
        } catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

