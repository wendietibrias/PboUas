    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        // TODO add your handling code here:
        int selectedRow = kategoriTable.getSelectedRow();
        String id = kategoriTable.getValueAt(selectedRow,0).toString();
        
        boolean isDeleted = productCategoryController.handleDelete(id);
        
        if(isDeleted){
            JOptionPane.showMessageDialog(null, "Berhasil Menghapus Kategori");
            this.displayTableData();
        } else {
            JOptionPane.showMessageDialog(null, "Gagal Menghapus Kategori");
        }
    }//GEN-LAST:event_jButton3MouseClicked

