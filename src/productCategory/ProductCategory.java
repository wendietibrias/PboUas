    public void displayTableData(){
        try {
          productCategoryController = new ProductCategoryController();
          ArrayList<ProductCategoryModel> kategoriItems = productCategoryController.renderCategoriesLabel();
          
            DefaultTableModel tableModel = new DefaultTableModel();
            
            tableModel.addColumn("No");
            tableModel.addColumn("Nama");
            
            tableModel.setRowCount(0);
            
            for(int i = 0; i < kategoriItems.size(); i++){
                Object[] row = {
                    kategoriItems.get(i).id,
                    kategoriItems.get(i).name
                };
                tableModel.addRow(row);
            }
            
            kategoriTable.setModel(tableModel);
        } catch(Exception e){
          System.out.println(e.getMessage());
        }
    }
    
